    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        // TODO add your handling code here:
        try {
            Class.forName(driver);
            Connection kon = DriverManager.getConnection(database,user,pass);
            Statement stt = kon.createStatement();
            String SQL = "DELETE FROM data_infaq "+"WHERE "+"no='"+tablemodel.getValueAt(row, 0).toString()+"'";
            String ResetNo = "ALTER TABLE data_infaq DROP no";
            String ConsecutiveNo = "ALTER TABLE data_infaq ADD no INT NOT NULL AUTO_INCREMENT PRIMARY KEY FIRST";
            stt.executeUpdate(SQL);
            stt.execute(ResetNo);
            stt.execute(ConsecutiveNo);
            tablemodel.removeRow(row);
            stt.close();
            kon.close();
            membersihkan_teks();
        } 
        catch (Exception ex) {
            System.err.println(ex.getMessage());
        }
    }//GEN-LAST:event_btn_hapusActionPerformed

