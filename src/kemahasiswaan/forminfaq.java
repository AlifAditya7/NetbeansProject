    private void btn_ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ubahActionPerformed
        // TODO add your handling code here:
        if (txt_tanggal.getDateFormatString().isEmpty() || txt_admin.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"data tidak boleh kosong, silahkan dilengkapi");
            txt_admin.requestFocus();
        }
        else{
            try {
                Class.forName(driver);
                Connection kon = DriverManager.getConnection(database,user,pass);
                Statement stt = kon.createStatement();
                String SQL = "UPDATE `data_infaq` "+"SET `kencleng`='"+Integer.valueOf(txt_kencleng.getText())+"',"
                        +"`donatur`='"+Integer.valueOf(txt_donatur.getText())+"',"
                        +"`total`='"+total()+"',"
                        +"`nama_donatur`='"+txt_nama_donatur.getText()+"',"
                        +"`admin_ubah`='"+txt_admin.getText()+"',"
                        +"`tanggal`='"+dateChooser+"'"
                        +"WHERE "+"`no`='"+tablemodel.getValueAt(row, 0).toString()+"';";
                stt.executeUpdate(SQL);
                data[1] = txt_kencleng.getText();
                data[2] = txt_donatur.getText();
                data[3] = String.valueOf(total());
                data[4] = txt_nama_donatur.getText();
                data[6] = txt_admin.getText();
                data[7] = dateChooser;
                tablemodel.removeRow(row);
                tablemodel.insertRow(row, data);
                stt.close();
                kon.close();
                membersihkan_teks();
                nonaktif_teks();
            } 
            catch (Exception ex) {
                System.err.println(ex.getMessage());
            }
        }
    }//GEN-LAST:event_btn_ubahActionPerformed

